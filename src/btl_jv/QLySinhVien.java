    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        try {
            
            sv=new Sinhvien(gettxtMaSV().getText(), gettxtTen().getText(), gettxtGVHD().getText(), 
                    gettxtTrangThai().getText(), cbxNganh.getSelectedItem().toString(), 
                    cbxCoSoTT.getSelectedItem().toString());
            if(!dssv.contains(sv)){
                dssv.add(sv);
                
                loadTableSV();
                JOptionPane.showMessageDialog(QLySinhVien.this, "Thêm sinh viên thành công", 
                        "", JOptionPane.INFORMATION_MESSAGE);
                
            }else{
                JOptionPane.showConfirmDialog(QLySinhVien.this, "Thông tin sinh viên đã tồn tại ", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(QLySinhVien.this, "Có lỗi: "+e.toString(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnThemActionPerformed

   
