    public JTextField gettxtMaSV() throws Exception{
        if(txtMaSV.getText().equals("")){
            throw  new Exception("Không được để trống mã sinh viên!");
        }
        if(dssv.contains(new Sinhvien(txtMaSV.getText().trim().toUpperCase()))){
            throw new Exception("Ma sinh vien da ton tai");
        }
        return txtMaSV;
    }
    
